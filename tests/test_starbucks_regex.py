    "スターバックス ラテ",
    "スターバックスラテ",
    "ドリップ コーヒー",
    "ドリップコーヒー",
    "カフェ モカ",
    "カフェモカ",
    "ホワイト モカ",
    "ホワイトモカ",
    "抹茶 ティー ラテ",
    "抹茶ティーラテ",
    "ほうじ茶 ティー ラテ",
    "チャイ ティー ラテ",
    "アール グレイ ティー ラテ",
    "コーヒー フラペチーノ",
    "キャラメル フラペチーノ",
    "ダーク モカ チップ フラペチーノ",
    "ダーク モカ チップ クリーム フラペチーノ",
    "抹茶 クリーム フラペチーノ",
    "バニラ クリーム フラペチーノ",
    "マンゴー パッション ティー フラペチーノ",
    "カプチーノ",
    
    # サイズ + ドリンク
    "ショート カフェ モカ",
    "トール スターバックス ラテ",
    "グランデ ホワイト モカ",
    "ベンティ ドリップ コーヒー",
    
    # サイズ + ドリンク + 温度
    "トール スターバックス ラテ ホット",
    "グランデ キャラメル マキアート アイス",
    "ショート カプチーノ エクストラホット",
    
    # カフェイン変更 + ドリンク
    "ディカフェ ドリップ コーヒー",
    "デカフェ スターバックス ラテ",
    "リストレット キャラメル マキアート",
    
    # カスタマイズ (1つ) + ドリンク (スペースあり・なし、新シロップ)
    "エクストラホイップ ダークモカチップフラペチーノ",
    "バニラシロップ追加 スターバックスラテ",
    "バニラ シロップ 追加 スターバックス ラテ",
    "キャラメルシロップ変更 キャラメルマキアート",
    "豆乳変更 カフェ モカ",
